package ads.poo;

public class Motor {
    
    private int cilindrada;
    private int horse;

    public Motor(int cilindrada, int horse) {
        this.cilindrada = cilindrada;
        this.horse = horse;
    }

    public int getCilindrada() {
        return cilindrada;
    }

    public void setCilindrada(int cilindrada) {
        this.cilindrada = cilindrada;
    }

    public int getHorse() {
        return horse;
    }

    public void setHorse(int horse) {
        this.horse = horse;
    }

    
}
